namespace addressbook_web_tests_zuev
{
    [TestFixture]
    public class ContactCreationTest
    {
        private IWebDriver driver;
        private StringBuilder verificationErrors;
        private string baseURL;
        private bool acceptNextAlert = true;

        [SetUp]
        public void SetupTest()
        {
            driver = new FirefoxDriver();
            baseURL = "http://localhost/addressbook/";
            verificationErrors = new StringBuilder();
        }

        [TearDown]
        public void TeardownTest()
        {
            try
            {
                driver.Quit();
            }
            catch (Exception)
            {
                // Ignore errors if unable to close the browser
            }
            Assert.AreEqual("", verificationErrors.ToString());
        }

        [Test]
        public void ContactCreationsTest()
        {
            OpenHomePage();
            Login(new AccountData("admin","secret"));
            InitContacktCreate();
            ContactData contact = new ContactData("aaa");
         //   contact.Firstname = "ddd";
            contact.Lastname = "fff";
            contact.Middlename = "fff";
            contact.Nickname = "fff";
            contact.Title = "fff";
            contact.Company = "fff";
            contact.Address = "fff";
            contact.Home = "fff";
            contact.Mobile = "fff";
            contact.Work = "fff";
            contact.Fax = "fff";
            contact.Email = "fff";
            contact.Homepage = "fff";
            FillContactForm(contact);
            GoToHomePage();
            Logout();
        }

        private void Logout()
        {
            driver.FindElement(By.LinkText("Logout")).Click();
        }

        private void GoToHomePage()
        {
            driver.FindElement(By.LinkText("home")).Click();
        }

